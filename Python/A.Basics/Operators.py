a,b=10,20
print(a+b)  # Addition
print(a-b)  # Subraction
print(a*b)  # Multiplication
print(a/b)  # Division[O/p in float values]
print(a//b) # Division[O/p in integer values]
print(a%b)  # Modulo[Remainder]
print(a**b) # Square the number

